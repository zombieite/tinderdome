<p class="location">HOUSE EXTERIOR - NIGHT</p>

<p class="p1">
All of the sounds stop and we have a brief silence. Zed is standing alone in front of Bruce's and Debby's house. He's about to knock, but then he stops himself. Instead, he whistles the same birdsong that he heard Bruce whistle to Debby earlier that day.</p>

<p class="camera">CUT TO:</p>
<p class="location">HOUSE INTERIOR, BEDROOM - NIGHT</p>

<p class="p1">Debby hears the whistle and looks up in delight. She stops playing the music box, leaving the last two notes of Happy Birthday unplayed. She whistles back, runs to the front door, and opens it. We cut to Zed's POV of Debby's face as the door opens.</p>

<p class="camera">CUT TO:</p>
<p class="location">OIL DONKEY COMPOUND - NIGHT</p>

<p class="p1">Bruce and the other former employees of 7 Sisters Petroleum are hard at work building Pappagallo's compound. Saws are grinding and sparks are flying. Pappagallo is approached by an employee who informs him about something we can't hear. He walks across the compound and takes Bruce aside with a solemn look on his face. This scene is intentionally written and shot as an homage to the scene in Mad Max 2 in which Wez rages over the death of the Golden Youth.</p>

<p class="character">PAPPAGALLO</p>
<p class="p3"><div class="dialogue">Bruce, I don't know how to tell you this, but I just got word that... that Debby is uh... Debby was uh... killed.</div></p>
<p class="p1">Bruce's emotions go from sadness to primal rage, and he attacks Pappagallo.</p>
<p class="character">BRUCE</p>
<p class="p3"><div class="dialogue">Zed. Zed did this! I'm going to kill him! I'm going to kill all of you! Kill! Kill! Kill!</div></p>
<p class="p1">The other compound dwellers restrain Bruce. In the fray, Bruce's hat falls off, revealing that his hair is cut into a red mohawk.</p>

<p class="camera">CUT TO:</p>
<p class="location">7SP FIELD OFFICE INTERIOR - NIGHT</p>

<p class="p1">We see Pappagallo's employee list, burning. We see Bruce's name and address, as we did earlier. But now we see that there is a section for &quot;Notes&quot; as well. The notes section beside Bruce's name reads, &quot;Hometown--Werrington NSW (nickname 'Wez').&quot;</p>

<p class="camera">CUT TO:</p>
<p class="location">HOUSE INTERIOR, KITCHEN - NIGHT</p>

<p class="p1">Zed bends over. He takes the music box from Debby's dead hand and puts it into his pocket of trinkets.</p>

