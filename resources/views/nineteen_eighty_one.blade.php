<p class="camera">CUT TO:</p>
<p class="location">HOUSE INTERIOR, BEDROOM - MORNING, JANUARY 1981</p>

<p class="p1">We hear the voice of a <a href="/1981/radio_announcer_with_music.mp3">RADIO ANNOUNCER</a> with an Aussie accent.</p>

<p class="character">RADIO ANNOUNCER</p>
<p class="p3"><div class="dialogue">In the US territory of Puerto Rico, explosions destroyed several Air National Guard aircraft. Police suspect it was yet another act of terrorism in a wave of violent rebellion sweeping the US.... Researchers in South Africa have identified several new cases of a mysterious and deadly immune deficiency disease affecting young men. The researchers suspect that the mens' illnesses are all being caused by the same unidentified virus.</div></p>

<p class="p1">DEBBY, a punk-rock girl with long brassy blonde hair, sits propped up in bed with a music box on her pregnant tummy. She is turning the crank of a music box, playing Happy Birthday. Debby doesn't play the last three notes of Happy Birthday because she is interrupted by the sound of a ringing timer coming from the kitchen. She sets the music box down and leaves the room. We can now see BRUCE asleep in the same bed. He slept in a work cap and a denim work vest. Inexplicably, on his bottom half Bruce is wearing only his underwear, and his underwear is nothing more than a g-string that shows off his buttocks. Bruce's cap and vest have been heavily personalised to make them look more punk. On one side of Bruce's vest there is a nametag patch. On the other side there is a patch with the company logo of 7 Sisters Petroleum. Above the 7SP patch Bruce has written &quot;I DRINK&quot; [7 SISTERS PETROLEUM].</p>

